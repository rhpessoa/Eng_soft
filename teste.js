function somar(a, b) {
    total = a + b
    return total
}

function subtrair(a, b) {
    total = a - b
    return total
}

function dividir(a, b) {
    total = a / b
    return total
}

function multiplicar(a, b) {
    total = a * b
    return total
}

module.exports = { somar, subtrair, dividir, multiplicar }